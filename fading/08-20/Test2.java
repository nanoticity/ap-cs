public class Test2 {
    public static void main(String[] args) {
        // int vars
        int int1 = 1;
        int int2 = 2;
        System.out.println(int1 + " " + int2);

        // double vars
        float float1 = 1.1F;
        float float2 = 2.2F;
        System.out.println(float1 + " " + float2);

        // char vars
        char char1 = 'A';
        char char2 = 'B';
        System.out.println(char1 + " " + char2);

        // double vars
        double double1 = 1.1;
        double double2 = 2.2;
        System.out.println(double1 + " " + double2);

        // boolean vars
        boolean bool1 = false;
        boolean bool2 = true;
        System.out.println(bool1 + " " + bool2);
    
        // int addition
        int result1 = int1 + int2;
        System.out.println(result1);

        // float addition
        float result2 = float1 + float2;
        System.out.println(result2);

        // double addition
        double result3 = double1 + double2;
        System.out.println(result3);

    }
}
